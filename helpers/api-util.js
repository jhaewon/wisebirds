export function getDuplicate(data) {
  const allUsers = getUsers();

  return allUsers.content.some((item) => item.email === data);
}

export function postUser(data) {
  const allUsers = getUsers();
  const id = allUsers.total_elements + 1;
  const temp = {
    email: data.id,
    name: data.name,
    id: id,
    last_login_at: "",
  };

  allUsers.content.splice(0, 0, temp);

  return allUsers;
}

export function patchUser(data) {
  const allUsers = getUsers();

  const newList = allUsers.content.map((item) => {
    if (item.email === data.id) {
      return { ...item, name: data.name };
    }
    return item;
  });

  allUsers.content = newList;
  return allUsers;
}
