export function patchCampaign(id, value) {
  const allCampaigns = getCampaigns();

  const newList = allCampaigns.content.map((item) => {
    if (item.id === id) {
      const updatedItem = {
        ...item,
        enabled: !value,
      };
      return updatedItem;
    }
    return item;
  });

  allCampaigns.content = newList;
  return newList;
}

